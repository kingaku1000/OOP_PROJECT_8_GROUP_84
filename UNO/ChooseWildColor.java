    public CardInfo.Color choseColor(CardInfo card){
        if(card.getColor() == CardInfo.Color.Wild){
            this.setBounds(100,150,450,400);
            this.setVisible(true);
            this.setResizable(false);
        }
        
        return card.getColor();
    }

