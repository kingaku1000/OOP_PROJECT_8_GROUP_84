    private void blueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blueActionPerformed
          wildColor = CardInfo.Color.Blue;
        JLabel message = new JLabel("COLOR TO BE PLAYED IS BLUE NOW!!");
        message.setFont(new Font("Calibri",Font.BOLD,24));
        JOptionPane.showMessageDialog(null,message);
        allow = true;
        this.dispose(); 
        popUp.declaredColor = CardInfo.Color.Blue;
        popUp.gameStage.setPlayerIdName(popUp.game.getCurrentPlayer()); 
        popUp.gameStage.setButtonIcons();
        popUp.discardPile.setIcon(new javax.swing.ImageIcon("C:\\Users\\91870\\OneDrive\\Documents\\NetBeansProjects\\UNO\\src\\main\\java\\UNO\\cardsImage\\" + popUp.game.getTopCardImage()));
        popUp.game.setCardColor(CardInfo.Color.Blue);
        popUp.dispose();
    }//GEN-LAST:event_blueActionPerformed

